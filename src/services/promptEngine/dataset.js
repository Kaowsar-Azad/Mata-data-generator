// Load custom categories from localStorage (if in browser)
if (typeof window !== 'undefined') {
  try {
    const savedMain = localStorage.getItem('custom_main_categories');
    if (savedMain) {
      const parsed = JSON.parse(savedMain);
      for (const [group, subs] of Object.entries(parsed)) {
        if (!mainCategories[group]) {
          mainCategories[group] = [];
        }
        for (const sub of subs) {
          if (!mainCategories[group].includes(sub)) {
            mainCategories[group].push(sub);
          }
        }
      }
    }
  } catch (e) {
    console.error('Failed to load custom_main_categories:', e);
  }
}

// Programmatically build the categories object
export const categories = {};
for (const [group, subs] of Object.entries(mainCategories)) {
  for (const sub of subs) {
    categories[sub] = generateData(sub, group);
  }
}

if (typeof window !== 'undefined') {
  try {
    const savedCategories = localStorage.getItem('custom_categories');
    if (savedCategories) {
      Object.assign(categories, JSON.parse(savedCategories));
    }
  } catch (e) {
    console.error('Failed to load custom_categories:', e);
  }
}

export function addCustomMainCategory(name) {
  if (mainCategories[name]) return;
  mainCategories[name] = [];
  if (typeof window !== 'undefined') {
    localStorage.setItem('custom_main_categories', JSON.stringify(mainCategories));
  }
}

export function addCustomSubCategory(mainCat, subCat) {
  if (!mainCategories[mainCat]) return;
  if (mainCategories[mainCat].includes(subCat)) return;
  mainCategories[mainCat].push(subCat);
  
  // Generate default data based on parent group
  categories[subCat] = generateData(subCat, mainCat);
  
  if (typeof window !== 'undefined') {
    localStorage.setItem('custom_main_categories', JSON.stringify(mainCategories));
    localStorage.setItem('custom_categories', JSON.stringify(categories));
  }
}



