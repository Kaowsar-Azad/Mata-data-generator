// Load custom styles, lighting, and camera angles from localStorage
if (typeof window !== 'undefined') {
  try {
    const customStyles = JSON.parse(localStorage.getItem('custom_styles') || '[]');
    customStyles.forEach(s => { if (!styles.includes(s)) styles.push(s); });
    
    const customLighting = JSON.parse(localStorage.getItem('custom_lighting') || '[]');
    customLighting.forEach(l => { if (!lighting.includes(l)) lighting.push(l); });
    
    const customCamera = JSON.parse(localStorage.getItem('custom_camera_angles') || '[]');
    customCamera.forEach(c => { if (!cameraAngles.includes(c)) cameraAngles.push(c); });
  } catch (e) {
    console.error('Failed to load custom styles/lighting/camera:', e);
  }
}

export function addCustomStyle(name) {
  if (styles.includes(name)) return;
  styles.push(name);
  if (typeof window !== 'undefined') {
    const custom = JSON.parse(localStorage.getItem('custom_styles') || '[]');
    if (!custom.includes(name)) {
      custom.push(name);
      localStorage.setItem('custom_styles', JSON.stringify(custom));
    }
  }
}

